def aumentar(preco, porcent, format=False):
        return preco + (preco*porcent/100)

def diminuir(preco, porcent, format=False):
        return preco - (preco*porcent/100)

def dobro(preco, format=False):
        return preco * 2

def metade(preco, format=False):
        return preco / 2
        